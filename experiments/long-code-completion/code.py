def compare_groups(
    a: List[Tuple[int, float, float]],
    b: List[Tuple[int, float, float]],
    title: str,
    top_k: int = 10,
) -> None:
    if len(a) != len(b):
        raise ValueError(f"Group sizes differ: {len(a)} vs {len(b)}")

    rows = []
    for (case_id_a, es_a, em_a), (case_id_b, es_b, em_b) in zip(a, b):
        if case_id_a != case_id_b:
            raise ValueError(
                f"Case id mismatch at aligned position: {case_id_a} vs {case_id_b}"
            )

        rows.append(
            {
                "case_id": case_id_a,
                "es_diff": es_b - es_a,
                "em_diff": em_b - em_a,
                "es_a": es_a,
                "es_b": es_b,
                "em_a": em_a,
                "em_b": em_b,
            }
        )

    print("=" * 90)
    print(title)
    print("=" * 90)

    top_es = sorted(rows, key=lambda x: x["es_diff"], reverse=True)[:top_k]
    print(f"\nTop {top_k} by ES difference (b - a):")
    for rank, item in enumerate(top_es, 1):
        print(
            f"{rank:>2}. case={item['case_id']:>3}  "
            f"es_diff={item['es_diff']:.6f}  "
            f"(b={item['es_b']:.6f} - a={item['es_a']:.6f})"
        )

    top_em = sorted(rows, key=lambda x: x["em_diff"], reverse=True)[:top_k]
    print(f"\nTop {top_k} by EM difference (b - a):")
    for rank, item in enumerate(top_em, 1):
        print(
            f"{rank:>2}. case={item['case_id']:>3}  "
            f"em_diff={item['em_diff']:.6f}  "
            f"(b={item['em_b']:.6f} - a={item['em_a']:.6f})"
        )


def main() -> None:
    compare_groups(GROUP1, GROUP2, "2 - 1")
    compare_groups(GROUP2, GROUP1, "1 - 2")
    compare_groups(GROUP3, GROUP2, "2 - 3")


if __name__ == "__main__":
    main()
